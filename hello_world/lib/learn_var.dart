void testVar() {
  //1. 基本规则
  //var 是一个类型推断关键字，其实际类型由首次赋值确定（编译时固定）。
  var t = "hi world";
  //后续赋值必须与首次推断的类型兼容，否则会编译报错。
  t = "1000";
  //t = 1000;  类型error

  //2. 特殊情况：未初始化时
  //如果 var 变量未初始化，Dart 会将其类型推断为 dynamic，此时可以赋任意值（但通常应避免这种用法）：
  var v; // 类型为 dynamic
  v = 10; // 合法
  v = 'text'; // 合法（但失去类型安全）
  print(v);
}
